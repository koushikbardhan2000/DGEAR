        <h2>User Instructions</h2>
        <img class="embedded-img" src="img/userguide.jpg" alt="User Guide for DGEAR Web-tool">
        <strong>1. Open the Webtool</strong>
        <p>The homepage will be displayed, showcasing information on how the web tool works and the navigation panel.</p>
        
        <strong>2. Navigate Through</strong>
        <p>Use the navigation panel (located at the top or bottom) to access the <strong>Analysis</strong> tab or any other relevant sections.</p>
        
        <strong>3. Upload Data</strong>
        <ul>
            <li>Navigate to the <strong>Analysis</strong> tab.</li>
            <li>Select the <strong>Choose File</strong> option to upload a gene expression file from your local machine.</li>
            <li>Click the <strong>Upload</strong> button to proceed.</li>
        </ul>
        
        <strong>4. Set Input Parameters</strong>
        <ul>
            <li>Provide the required input parameters for analysis.</li>
            <li>Specify control and experimental column ranges.</li>
            <li>Set the <strong>alpha value</strong> and <strong>voting cutoff</strong>.</li>
        </ul>
        
        <strong>5. Submit Request & Process Data</strong>
        <p>Click <strong>Submit Request</strong> to initiate the analysis process.</p>
        
        <strong>6. Explore Content</strong>
        <ul>
            <li>Navigate through the <strong>Results Page</strong> to explore content, view plots, and access downloadable files or available resources.</li>
        </ul>
