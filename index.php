	    <h1>Welcome to DGEAR-Web Dashboard for Differential Gene Expression Analysis</h1>
        <p>DEG-Ensembl is a web-based platform designed for ensemble-based integrative analysis, enabling the prediction of differentially expressed genes (DEGs) from gene expression data. The tool enhances the usability and accessibility of bioinformatics analysis for researchers, scientists, and other users in the field.</p>
        
        <h2>Key Features</h2>
        <ul>
            <li><strong>Cross-Platform Compatibility:</strong> GUI-based web tools can be accessed across different operating systems and devices, eliminating platform dependencies.</li>
            <li><strong>User-Friendly Interface:</strong> GUI-based tools provide a user-friendly interface that allows users to interact using visual elements such as buttons, forms, and menus. This makes it easier for users without extensive programming knowledge to utilize bioinformatics tools and perform complex analyses.</li>
            <li><strong>Accessibility:</strong> GUI-based tools make bioinformatics analysis accessible to a wider audience without requiring advanced programming skills or command-line expertise.</li>
            <li><strong>Efficiency and Time-Saving:</strong> GUI-based tools streamline the bioinformatics analysis process, reducing the time and effort required for data preprocessing.</li>
        </ul>
        
        <h2>Project Background</h2>
        <p>This web tool was developed as part of the project titled <em>"To Develop an Ensemble Framework for Differential Expression Analysis."</em> It was submitted to the <strong>University of North Bengal</strong> in partial fulfillment of the requirements for the <strong>Master of Science in Bioinformatics</strong> by <strong>Koushik Bardhan (2022-2023)</strong>, under the supervision and guidance of <strong>Dr. Chiranjib Sarkar</strong>, Department of Bioinformatics, University of North Bengal, West Bengal.</p>
        
        <h2>User Instructions</h2>
        <h2>1. Open the Webtool</h2>
        <p>The homepage will be displayed, showcasing information on how the web tool works and the navigation panel.</p>
        
        <h2>2. Navigate Through</h2>
        <p>Use the navigation panel (located at the top or bottom) to access the <strong>Analysis</strong> tab or any other relevant sections.</p>
        
        <h2>3. Upload Data</h2>
        <ul>
            <li>Navigate to the <strong>Analysis</strong> tab.</li>
            <li>Select the <strong>Choose File</strong> option to upload a gene expression file from your local machine.</li>
            <li>Click the <strong>Upload</strong> button to proceed.</li>
        </ul>
        
        <h2>4. Set Input Parameters</h2>
        <ul>
            <li>Provide the required input parameters for analysis.</li>
            <li>Specify control and experimental column ranges.</li>
            <li>Set the <strong>alpha value</strong> and <strong>voting cutoff</strong>.</li>
        </ul>
        
        <h2>5. Submit Request & Process Data</h2>
        <p>Click <strong>Submit Request</strong> to initiate the analysis process.</p>
        
        <h2>6. Explore Content</h2>
        <ul>
            <li>Navigate through the <strong>Results Page</strong> to explore content, view plots, and access downloadable files or available resources.</li>
        </ul>
        
